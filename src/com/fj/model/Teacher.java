package com.fj.model;

public class Teacher {

	private int teacher_id;
	private String teacher_name;
	private int clazz_id;
	private int teacher_age;
	private int teacher_sex;
	private int teacher_pref;
	public int getTeacher_id() {
		return teacher_id;
	}
	public void setTeacher_id(int teacher_id) {
		this.teacher_id = teacher_id;
	}
	public String getTeacher_name() {
		return teacher_name;
	}
	public void setTeacher_name(String teacher_name) {
		this.teacher_name = teacher_name;
	}
	public int getClazz_id() {
		return clazz_id;
	}
	public void setClazz_id(int clazz_id) {
		this.clazz_id = clazz_id;
	}
	public int getTeacher_age() {
		return teacher_age;
	}
	public void setTeacher_age(int teacher_age) {
		this.teacher_age = teacher_age;
	}
	public int getTeacher_sex() {
		return teacher_sex;
	}
	public void setTeacher_sex(int teacher_sex) {
		this.teacher_sex = teacher_sex;
	}
	public int getTeacher_pref() {
		return teacher_pref;
	}
	public void setTeacher_pref(int teacher_pref) {
		this.teacher_pref = teacher_pref;
	}
	
}
